#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			t;
	unsigned char	*ptr;

	ptr = (unsigned char *)s;
	t = 0;
	while (t < n)
	{
		ptr[t] = '\0';
		t++;
	}
}
